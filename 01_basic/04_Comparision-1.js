//$$$$$$$$$$$$  EASY COMPARISIONS --> b/w SAME TYPE  $$$$$$$$$$$$$$$$$$$$

//Comparision between --> Numbers       
console.log(2==2);
console.log(2 != 2);
console.log(2>2);
console.log(2<2);
console.log(2>=2);
console.log(2<=2);
console.log("-----------------------");

//Comparision between --> Boolean values
console.log(true == true);
console.log(true == false);
console.log("-----------------------");

//We should always use these   ABOVE COMPARISIONS
//because it is                EASY TO UNDERSTAND

//$$$$$$$$$$$  EASY COMPARISIONS --> b/w SAME TYPE  $$$$$$$$$$$$$$$$$$$$$$$

